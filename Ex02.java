public class Ex02 {
  
        public static void main(String[] args) {
            // Criar variáveis do tipo literal e armazenar os valores
            String primeiraPalavra = "CEDUP";
            String segundaPalavra = "INFORMÁTICA";
    
            // Concatenar as duas variáveis e armazenar em uma terceira variável
            String resultado = primeiraPalavra + " " + segundaPalavra;
    
            // Exibir o resultado no console
            System.out.println("Resultado da concatenação: " + resultado);
            
        }
    }
    

